function isLanguagePipelineV2Enabled(language: SupportedLanguage): boolean {
  if (language === "hi" || language === "te" || language === "kn" || language === "mr" || language === "ta" || language === "gu") return true;
  if (language === "en") return false;
  return false;
}

function parseBirthTime(rawTime: string): { hour: number; min: number } {
  const input = String(rawTime || "").trim();
  if (!input) throw new Error("Invalid birth time: empty");
  const ampm = input.match(/\b(am|pm)\b/i)?.[1]?.toLowerCase();
  const cleaned = input.replace(/\s*(am|pm)\s*/i, "");
  const parts = cleaned.split(":").map((x) => Number(x));
  let hour = parts[0];
  const min = parts.length > 1 ? parts[1] : 0;
  if (!Number.isFinite(hour) || !Number.isFinite(min)) {
    throw new Error(`Invalid birth time format: "${rawTime}"`);
  }
  if (ampm) {
    hour = hour % 12;
    if (ampm === "pm") hour += 12;
  }
  return { hour, min };
}

function stripAiDisclosure<T>(value: T): T {
  const cleanText = (text: string): string =>
    text
      .replace(/\bAI[- ]generated\b/gi, "generated")
      .replace(/\bgenerated by AI\b/gi, "generated")
      .replace(/\bAI[- ]powered\b/gi, "")
      .replace(/\bartificial intelligence\b/gi, "")
      .replace(/[ \t]{2,}/g, " ")
      .trim();

  if (typeof value === "string") return cleanText(value) as T;
  if (Array.isArray(value)) return value.map((item) => stripAiDisclosure(item)) as T;
  if (value && typeof value === "object") {
    const obj = value as Record<string, unknown>;
    const out: Record<string, unknown> = {};
    for (const [key, val] of Object.entries(obj)) out[key] = stripAiDisclosure(val);
    return out as T;
  }
  return value;
}

// ── Report text cleanup ──────────────────────────────────────────────────────

/**
 * Detect whether a string is mostly gibberish.
 * Returns true if the text has hallmarks of AI-garbled output.
 */
function isGibberishText(text: string): boolean {
  if (!text || text.length < 10) return false;

  // 1. Excessive consecutive consonants (no language has 6+ consonants in a row)
  if (/[bcdfghjklmnpqrstvwxyz]{6,}/i.test(text)) return true;

  // 2. Same character repeated 4+ times
  if (/(.)\1{3,}/.test(text)) return true;

  // 3. Mostly non-ASCII non-Devanagari/Telugu/Kannada control/garbage characters
  const controlChars = text.replace(/[\x20-\x7E\u0900-\u097F\u0C00-\u0CFF\n\r\t]/g, "");
  if (controlChars.length > text.length * 0.3) return true;

  return false;
}

/**
 * Clean a single text string — language-agnostic:
 *  - Remove zero-width / invisible Unicode
 *  - Collapse repeated characters (4+ → 2)
 *  - Collapse excess whitespace
 *  - Replace em-dashes (—) with regular dashes (-) to avoid AI-generated look
 *
 * NOTE: We do NOT strip Devanagari/Telugu from English reports. If the content
 * is valid (e.g., Sanskrit terms), we keep it. Only truly garbled sequences
 * (detected by isGibberishText) get flagged.
 */
function cleanReportText(text: string): string {
  if (!text || typeof text !== "string") return text;

  let cleaned = text;

  // Remove zero-width chars, soft hyphens, and other invisible Unicode
  cleaned = cleaned.replace(/[\u200B-\u200F\u2028-\u202F\u00AD\uFEFF]/g, "");

  // Replace em-dashes (—) with regular dashes (-) — avoids AI-generated look
  cleaned = cleaned.replace(/\u2014/g, "-");
  // Replace en-dashes (–) with regular dashes too
  cleaned = cleaned.replace(/\u2013/g, "-");

  // Remove same character repeated 4+ times (garbled output)
  cleaned = cleaned.replace(/(.)\1{3,}/g, "$1$1");

  // Collapse multiple spaces / newlines
  cleaned = cleaned.replace(/[ \t]{2,}/g, " ").replace(/\n{3,}/g, "\n\n").trim();

  return cleaned;
}

/**
 * Recursively clean all text fields in the report (any language).
 * Replaces em-dashes, removes invisible chars, fixes garbled sequences.
 * Does NOT remove valid script content (Devanagari, Telugu, Kannada, Latin).
 */
function cleanReportTexts<T>(value: T, parentKey = ""): T {
  if (typeof value === "string") {
    // If the entire string is true gibberish, return empty
    if (isGibberishText(value)) {
      const cleaned = cleanReportText(value);
      if (cleaned.length < 5) return "" as unknown as T;
      return cleaned as unknown as T;
    }
    return cleanReportText(value) as unknown as T;
  }
  if (Array.isArray(value)) {
    return value
      .map((item) => cleanReportTexts(item, parentKey))
      .filter((item) => {
        if (typeof item === "string" && item.trim() === "") return false;
        return true;
      }) as unknown as T;
  }
  if (value && typeof value === "object") {
    const obj = value as Record<string, unknown>;
    const out: Record<string, unknown> = {};
    for (const [key, val] of Object.entries(obj)) {
      out[key] = cleanReportTexts(val, key);
    }
    return out as T;
  }
  return value;
}

