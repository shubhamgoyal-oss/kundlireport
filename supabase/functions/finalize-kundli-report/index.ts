serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  const supabaseAdmin = createClient(
    Deno.env.get("SUPABASE_URL") ?? "",
    Deno.env.get("SUPABASE_SERVICE_ROLE_KEY") ?? ""
  );

  let jobId: string | undefined;

  try {
    const body = await req.json();
    jobId = body.jobId;
    console.log(`\uD83C\uDFC1 [FINALIZE-JOB] Starting finalization for job: ${jobId}`);

    // Load job record with translated report
    const { data: job, error: fetchError } = await supabaseAdmin
      .from("kundli_report_jobs")
      .select("*")
      .eq("id", jobId)
      .single();

    if (fetchError || !job) {
      console.error("\u274C [FINALIZE-JOB] Job not found:", jobId);
      return new Response(JSON.stringify({ error: "Job not found" }), { status: 404 });
    }

    let report = job.report_data as Record<string, any>;
    if (!report) {
      const nowIso = new Date().toISOString();
      const existingDebugSummary =
        job.debug_summary && typeof job.debug_summary === "object"
          ? { ...(job.debug_summary as Record<string, any>) }
          : {};
      const priorRequeueAttempts = Number(existingDebugSummary.missingReportDataRequeueAttempts || 0);
      const canRequeueTranslation = priorRequeueAttempts < 1;
      const waitingPhase = canRequeueTranslation
        ? "Waiting for report data — requeueing translation"
        : "Waiting for report data — awaiting prior stage";

      existingDebugSummary.missingReportDataRequeueAttempts = canRequeueTranslation
        ? priorRequeueAttempts + 1
        : priorRequeueAttempts;
      existingDebugSummary.lastMissingReportDataAt = nowIso;
      existingDebugSummary.lastMissingReportDataPhase = String(job.current_phase || "");

      const nextProgress = Math.max(Number(job.progress_percent || 0), 85);

      const { error: waitingUpdateError } = await supabaseAdmin
        .from("kundli_report_jobs")
        .update({
          status: "processing",
          current_phase: waitingPhase,
          progress_percent: nextProgress,
          heartbeat_at: nowIso,
          debug_summary: existingDebugSummary,
        })
        .eq("id", jobId);
      if (waitingUpdateError) {
        console.warn("[FINALIZE-JOB] Failed to mark waiting state for missing report_data:", waitingUpdateError.message);
      }

      await createJobEvent(supabaseAdmin, {
        jobId,
        phase: "Finalize waiting for report_data",
        eventType: "info",
        message: canRequeueTranslation
          ? "Missing report_data in finalize; requeued translation stage once"
          : "Missing report_data in finalize; requeue limit reached",
        metrics: { progress: nextProgress, requeueAttempt: priorRequeueAttempts + (canRequeueTranslation ? 1 : 0) },
      });

      if (canRequeueTranslation) {
        const stage2Url = `${Deno.env.get("SUPABASE_URL")}/functions/v1/translate-kundli-report`;
        const stage2Promise = fetch(stage2Url, {
          method: "POST",
          headers: {
            "Content-Type": "application/json",
            "Authorization": `Bearer ${Deno.env.get("SUPABASE_SERVICE_ROLE_KEY")}`,
          },
          body: JSON.stringify({ jobId }),
        }).catch((err) => {
          console.error("[FINALIZE-JOB] Failed to requeue translation stage:", err);
        });

        // @ts-ignore - EdgeRuntime is available in Supabase Edge Functions
        if (typeof EdgeRuntime !== "undefined" && EdgeRuntime.waitUntil) {
          // @ts-ignore
          EdgeRuntime.waitUntil(stage2Promise);
        }
      }

      return new Response(
        JSON.stringify({
          success: true,
          accepted: true,
          waitingForReportData: true,
          requeued: canRequeueTranslation,
          jobId,
        }),
        {
          status: 202,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        }
      );
    }

    const requestedLanguage = normalizeLanguage(job.language || "en");
    const useLanguagePipelineV2 = isLanguagePipelineV2Enabled(requestedLanguage);
    const effectiveGenLang: SupportedLanguage = useLanguagePipelineV2 ? requestedLanguage : "en";
    const activeLanguagePack = getLanguagePack(requestedLanguage);
    setAgentLanguageContext(effectiveGenLang);

    let totalTokens = report.tokensUsed || 0;
    const [year, month, day] = String(job.date_of_birth).split("-").map(Number);
    const { hour: localHour, min: localMin } = parseBirthTime(String(job.time_of_birth));

    // Convert birth time from local timezone to IST (UTC+5:30) for chart API
    // Seer chart API expects IST time (tzone field is ignored)
    const istOffsetMinutes = 5.5 * 60; // IST is UTC+5:30
    const localOffsetMinutes = (job.timezone || 0) * 60; // Local timezone offset in minutes
    const offsetDiffMinutes = istOffsetMinutes - localOffsetMinutes;

    let istHour = localHour;
    let istMin = localMin + offsetDiffMinutes;
    let istYear = year, istMonth = month, istDay = day;

    if (istMin >= 60) {
      istHour += Math.floor(istMin / 60);
      istMin = istMin % 60;
    } else if (istMin < 0) {
      istHour += Math.floor(istMin / 60);
      istMin = istMin % 60;
      if (istMin < 0) istMin += 60;
    }

    if (istHour >= 24) {
      istHour -= 24;
      const nextDay = new Date(year, month - 1, day + 1);
      istYear = nextDay.getFullYear();
      istMonth = nextDay.getMonth() + 1;
      istDay = nextDay.getDate();
    } else if (istHour < 0) {
      istHour += 24;
      const prevDay = new Date(year, month - 1, day - 1);
      istYear = prevDay.getFullYear();
      istMonth = prevDay.getMonth() + 1;
      istDay = prevDay.getDate();
    }

    // ── QA Validation ──────────────────────────────────────────────────────
    await updateJobStatus(supabaseAdmin, jobId!, "processing", "Running quality checks", 92, "progress");
    const qaResult = await runQaWithTimeout(report);
    if (qaResult.blockedContent.length > 0 || qaResult.issues.some((i: any) => i.severity === "critical")) {
      report = sanitizeReportContent(report);
    }
    report.qa = qaResult;

    // ── Text cleanup (ALL languages) ──────────────────────────────────────────
    // Remove em-dashes (→ regular dashes), invisible chars, garbled sequences.
    // Applied to ALL languages — does NOT strip valid script content.
    await updateJobStatus(supabaseAdmin, jobId!, "processing", "Cleaning report text", 94, "progress");
    report = cleanReportTexts(report);
    console.log("🧹 [FINALIZE-JOB] Report text cleanup complete (em-dashes, invisible chars, gibberish)");

    // ── Localization + Language QC ──────────────────────────────────────────
    report = localizeStructuredReportTerms(report, requestedLanguage);
    report.language = requestedLanguage;
    report = stripAiDisclosure(report);

    const languageQc = useLanguagePipelineV2
      ? runLanguageQc(report, requestedLanguage)
      : {
          language: requestedLanguage,
          packVersion: activeLanguagePack.version,
          generationMode: "native" as const,
          passed: true,
          summary: { sectionsChecked: 0, sectionsPassed: 0, totalScriptChars: 0, totalLetters: 0, overallScriptRatio: 0, overallLatinRatio: 0 },
          sections: [],
          leakSamples: [],
        };

    report.languageQc = languageQc;
    report.languageQcPassed = languageQc.passed;
    report.failureCode = languageQc.passed ? null : "LANGUAGE_QC_FAILED";

    if (!languageQc.passed) {
      const leakSummary = languageQc.leakSamples
        .slice(0, 5)
        .map((s) => `${s.section}:${s.token}`)
        .join(", ");
      const ratioSummary = (languageQc.sections || [])
        .filter((s: any) => !s.passed)
        .slice(0, 5)
        .map((s: any) => `${s.section}:script=${(Number(s.scriptRatio || 0) * 100).toFixed(1)}%,latin=${(Number(s.latinRatio || 0) * 100).toFixed(1)}%`)
        .join(", ");
      console.warn(`\u26A0\uFE0F [FINALIZE-JOB] LANGUAGE_QC_FAILED (non-fatal): ${leakSummary || ratioSummary || "localized output validation did not pass"}`);
      report.errors = [...(report.errors || []), `LANGUAGE_QC_FAILED: ${leakSummary || ratioSummary || "localized output validation did not pass"}`];
    }

    // ── Chart Fetching ─────────────────────────────────────────────────────
    try {
      await updateJobStatus(supabaseAdmin, jobId!, "processing", "Fetching Kundali charts", 97, "progress");
      const charts = await fetchKundaliCharts(istDay, istMonth, istYear, istHour, Math.floor(istMin), job.latitude, job.longitude, 5.5, requestedLanguage);
      report.charts = charts;
    } catch (chartErr) {
      console.warn("\u26A0\uFE0F [FINALIZE-JOB] Chart fetch failed (non-fatal):", chartErr);
      report.charts = [];
    }

    // ── Save completed report ──────────────────────────────────────────────
    report.tokensUsed = totalTokens;

    const extendedCompletion = await supabaseAdmin
      .from("kundli_report_jobs")
      .update({
        status: "completed",
        current_phase: "Complete",
        progress_percent: 100,
        report_data: report,
        generation_language_mode: useLanguagePipelineV2 ? "native" : "legacy",
        language_qc: report.languageQc || null,
        debug_summary: {
          languagePackVersion: activeLanguagePack.version,
          requestedLanguage,
          effectiveGenerationLanguage: effectiveGenLang,
          generationMode: useLanguagePipelineV2 ? "native" : "legacy",
          tokensUsed: totalTokens,
          qaScore: report.qa?.overallScore || null,
        },
        completed_at: new Date().toISOString(),
      })
      .eq("id", jobId);

    if (extendedCompletion.error) {
      console.warn("[FINALIZE-JOB] Extended completion update failed, using legacy payload:", extendedCompletion.error);
      await supabaseAdmin
        .from("kundli_report_jobs")
        .update({
          status: "completed",
          current_phase: "Complete",
          progress_percent: 100,
          report_data: report,
          completed_at: new Date().toISOString(),
        })
        .eq("id", jobId);
    }

    await createJobEvent(supabaseAdmin, {
      jobId: jobId!,
      phase: "Complete",
      eventType: "success",
      message: "Report generation completed",
      metrics: { progress: 100, tokensUsed: totalTokens },
    });

    await upsertKundliGeneratedReport(supabaseAdmin, {
      jobId: jobId!,
      language: requestedLanguage,
      generationMode: useLanguagePipelineV2 ? "native" : "legacy",
      status: "completed",
      reportSummary: {
        tokensUsed: totalTokens,
        qaScore: report.qa?.overallScore || null,
        errorCount: Array.isArray(report.errors) ? report.errors.length : 0,
        sections: {
          planets: Array.isArray(report.planets) ? report.planets.length : 0,
          houses: Array.isArray(report.houses) ? report.houses.length : 0,
        },
      },
    });

    console.log("\u2705 [FINALIZE-JOB] Report completed and saved successfully");
    return new Response(
      JSON.stringify({ success: true, jobId }),
      { headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  } catch (error) {
    console.error("\u274C [FINALIZE-JOB] Error:", error);

    if (jobId) {
      await createJobEvent(supabaseAdmin, {
        jobId,
        phase: "Finalize Error",
        eventType: "fail",
        message: error instanceof Error ? error.message : "Unknown error",
      });

      await supabaseAdmin
        .from("kundli_report_jobs")
        .update({
          status: "failed",
          failure_code: "FINALIZE_ERROR",
          error_message: error instanceof Error ? error.message : "Unknown error",
        })
        .eq("id", jobId)
        .then(({ error: updateErr }) => {
          if (updateErr) {
            return supabaseAdmin
              .from("kundli_report_jobs")
              .update({
                status: "failed",
                error_message: error instanceof Error ? error.message : "Unknown error",
              })
              .eq("id", jobId);
          }
        });
    }

    return new Response(
      JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});

async function updateJobStatus(
  supabase: any,
  jobId: string,
  status: string,
  phase: string,
  progress: number,
  eventType: "start" | "progress" | "success" | "fail" | "info" = "progress",
) {
  const withHeartbeat = await supabase
    .from("kundli_report_jobs")
    .update({
      status,
      current_phase: phase,
      progress_percent: progress,
      heartbeat_at: new Date().toISOString(),
    })
    .eq("id", jobId);
  if (withHeartbeat.error) {
    await supabase
      .from("kundli_report_jobs")
      .update({ status, current_phase: phase, progress_percent: progress })
      .eq("id", jobId);
  }

  await createJobEvent(supabase, {
    jobId,
    phase,
    eventType,
    message: `${status}: ${phase}`,
    metrics: { progress },
  });
}
