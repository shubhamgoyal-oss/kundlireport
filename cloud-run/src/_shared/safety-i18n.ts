/** Look up a localized safety string. Falls back to English if no entry for active language. */
export function safetyText(key: string, vars?: Record<string, string>): string {
  const lang = getAgentLanguage() as keyof L5;
  const entry = S[key];
  if (!entry) return key;
  let text = entry[lang] || entry.en;
  if (vars) {
    for (const [k, v] of Object.entries(vars)) {
      text = text.replaceAll(`{${k}}`, v);
    }
  }
  return text;
}
